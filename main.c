    printf("2. 500MB @200KES \n");
    printf("3. 1GB   @350KES \n");
    printf("4. 2GB   @600KES \n");

    //prompt user to enter choice
    printf("Enter your choice(1-4):");
    scanf("%d",&choice);

    //use switch-case
    switch(choice){
    case 1:
        printf("You selected 100MB. cost=50KES \n");
        break;
    case 2:
        printf("You selected 500MB. cost=200KES \n");
        break;
    case 3:
        printf("You selected 1 GB. cost=350KES \n");
        break;
    case 4:
        printf("You selected 2 GB. cost=600KES \n");
        break;
    default:
        printf("Invalid choice \n");
    }
